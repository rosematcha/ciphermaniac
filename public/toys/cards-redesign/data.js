/* Build a Limitless CDN thumbnail URL — same pattern the live site uses. */
window.thumbUrl = function thumbUrl(card, size = 'SM') {
  const code = String(card.set || '')
    .toUpperCase()
    .trim();
  const num = String(card.number || '')
    .trim()
    .padStart(3, '0');
  if (!code || !num) {
    return '';
  }
  return `https://limitlesstcg.nyc3.cdn.digitaloceanspaces.com/tpci/${code}/${code}_${num}_R_EN_${size}.png`;
};

/* Mocked context — synthetic deltas / prices to drive comparison-friendly mockups. */
window.SAMPLE_CARDS.forEach((card, i) => {
  const seed = (i * 17 + 13) % 41;
  const direction = i % 4 === 0 ? -1 : i % 5 === 0 ? 0 : 1;
  card.delta7d = Number((direction * (seed / 10)).toFixed(1));
  card.price = Number((0.2 + ((seed * 7) % 90) / 10).toFixed(2));
  card.deckCount = card.found;
});

/* Mode helper for the histogram column - which copy count is most common */
window.modeOf = function modeOf(dist) {
  if (!Array.isArray(dist) || !dist.length) {
    return 0;
  }
  return dist.reduce((best, cur) => (cur.percent > best.percent ? cur : best), dist[0]).copies;
};
